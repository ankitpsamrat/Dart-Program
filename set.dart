// Dart program to show the Sets concept
void main(List<String> args) {
// Declaring set in First Way
  var s1 = <String>{'Ankit'};

// Printing First Set
  print("Output of first set: $s1");

// Declaring set in Second Way
  Set<String> s2 = {'Ankit'};

// Printing Second Set
  print("Output of second set: $s2");
}
