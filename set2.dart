void main(List<String> args) {
// Declaring list with repeated value
  var v = ['Geeks', 'For', 'Geeks'];

// Printing List
  print("Output of the list is: $v");

// Declaring set with repeated value
  var v2 = <String>{'Geeks', 'For', 'Geeks'};

// Printing Set
  print("Output of the set is: $v2");
}
